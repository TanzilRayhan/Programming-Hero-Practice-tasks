//set new value
var price1 = 15;
var price2 = 20;

price1 = price1 + 20;
console.log(price1);

// Shorthand +=
// += means adding value to the existing value
price1 += 20;

// Shorthand -=
// += means deducting value from the existing value
price2 -= 5;
console.log(price2);

// Shorthand *=
price1 *= 2;

// Shorthand /=
price2 /= 5;

var age = 20;
// ++ -- add or deduct 1
age++;
age--;
console.log(age);
